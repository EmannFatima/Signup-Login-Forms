     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        // dd($request);
        // dd($request)->all();
        $data = $request->validate([
            'file' => 'required|mimes:pdf,xlxs,xlx,docx,doc,csv,txt,png,gif,jpg,jpeg|max:1000',
        ]);

        $file_path = time() . '.' . $request->file->getClientOriginalExtension();
        request()->file->move(public_path('uploads/file'), $file_path);
        $data['file'] = 'uploads/file/' . $file_path;

        Index::create($data);
        return redirect()->route('page.index');
    }

